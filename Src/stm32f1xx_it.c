/**
  * @brief This function handles TIM1 update interrupt.
  */
void TIM1_UP_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_IRQn 0 */

  /* USER CODE END TIM1_UP_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  /* USER CODE BEGIN TIM1_UP_IRQn 1 */
	if(ElecSwitchState == 1)
	{
		HAL_GPIO_WritePin(ElecSwitch_GPIO_Port,ElecSwitch_Pin,GPIO_PIN_SET);
	}
	else
	{
	 HAL_GPIO_WritePin(ElecSwitch_GPIO_Port,ElecSwitch_Pin,GPIO_PIN_RESET);
	}
	
	if(HAL_GPIO_ReadPin(GPIOB,FireWarn_Pin)==GPIO_PIN_RESET)
	{
		FireWarnCnt++;
		if(FireWarnCnt == 10)
		{
			if(HAL_GPIO_ReadPin(GPIOB,FireWarn_Pin)==GPIO_PIN_RESET)
			{
				FireWarn = 1;
			}
			FireWarnCnt = 0;
		}
	}
	else
	{
		FireWarn = 0;
		FireWarnCnt = 0;
	}
	
	

	if(HAL_GPIO_ReadPin(GPIOB,AirWarn_Pin)==GPIO_PIN_RESET)
	{
		AirWarnCnt++;
		if(AirWarnCnt == 10)
		{
			if(HAL_GPIO_ReadPin(GPIOB,AirWarn_Pin)==GPIO_PIN_RESET)
			{
				AirWarn = 1;
			}
			AirWarnCnt = 0;
		}
	}
	else
	{
		AirWarn = 0;
		AirWarnCnt = 0;
	}
	 
		if(FireWarn == 1 || AirWarn == 1)
		{
			HAL_GPIO_WritePin(GPIOC,Beep_Pin,GPIO_PIN_SET);
		}
		else
		{
			HAL_GPIO_WritePin(GPIOC,Beep_Pin,GPIO_PIN_RESET);
		}
	
	

	
	if(DoorState == 1)
	{
		TIM8->CCR1 = 1000;
	}
	else
	{
		TIM8->CCR1 =1500;
	}
	
	switch(FanState)
	{
		case 0 : FanMotor = 0; break;
		case 1 : FanMotor = 8000; break;
		case 2 : FanMotor = 10000; break;
		case 3 : FanMotor = 12000; break;
		case 4 : FanMotor = 14000; break;
		case 5 : FanMotor = 16000; break;
		default: FanMotor = 0; break;
	}
	
	TIM8->CCR2 = FanMotor;
	
	
  /* USER CODE END TIM1_UP_IRQn 1 */
}

